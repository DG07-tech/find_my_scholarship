const toLegacyGender = (genders) => {
  if (!genders || genders.includes("All")) return "Both";
  if (genders.length === 1) return genders[0];
  return "Both";
};

const toLegacyValue = (values) => {
  if (!values || values.length === 0 || values.includes("All")) return "All";
  return values.join("/");
};

const toLegacyResidence = (areas) => {
  if (!areas || areas.length === 0 || areas.includes("All")) return "Both";
  return areas.join("/");
};

const makeScholarship = (scheme) => ({
  ...scheme,
  gender: toLegacyGender(scheme.genders),
  state: scheme.states?.[0] || "All",
  areaOfResidence: toLegacyResidence(scheme.residenceAreas),
  casteCategory: toLegacyValue(scheme.casteCategories),
  annualIncome: scheme.maxAnnualIncome ?? 0,
  religion: toLegacyValue(scheme.religions),
  schemeDocuments: scheme.detailsUrl,
  siteLink: scheme.applicationUrl || scheme.sourceUrl,
});

